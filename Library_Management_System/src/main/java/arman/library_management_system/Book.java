package arman.library_management_system;

/**
 *
 * @author arman
 */
public class Book {

    private String title;
    private String author;

    public Book(String title, String author) {
        this.title = title;
        this.author = author;
    }

    public String getTitle() {
        return title;
    }

    @Override
    public String toString() {
        return "" + title + " by " + author;
    }

}
